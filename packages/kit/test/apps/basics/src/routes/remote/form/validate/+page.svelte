<script>
	import { my_form, my_form_2 } from './form.remote.js';
	import * as v from 'valibot';

	const schema = v.object({
		foo: v.picklist(['a', 'b', 'c']),
		bar: v.picklist(['d', 'e']),
		button: v.optional(v.literal('submitter'))
	});
	let submitter;
	let error = $state(false);
</script>

<form data-form-1 {...my_form.preflight(schema)} oninput={() => my_form.validate()}>
	{#each my_form.fields.foo.issues() as issue}
		<p>{issue.message}</p>
	{/each}

	<input {...my_form.fields.foo.as('text')} />

	{#each my_form.fields.bar.issues() as issue}
		<p>{issue.message}</p>
	{/each}

	<input {...my_form.fields.bar.as('text')} />

	<button>submit (imperative validation)</button>

	<button bind:this={submitter} {...my_form.fields.button.as('submit', 'incorrect_value')}>
		submit
	</button>
	{#each my_form.fields.button.issues() as issue}
		<p>{issue.message}</p>
	{/each}
</form>

<button
	id="trigger-validate"
	onclick={() => my_form.validate({ includeUntouched: true, submitter })}
>
	trigger validation
</button>

<form
	data-form-2
	{...my_form_2.enhance(async ({ submit }) => {
		error = false;
		try {
			await submit();
		} catch {
			error = true;
		}
	})}
>
	{#each my_form_2.fields.baz.issues() as issue}
		<p>{issue.message}</p>
	{/each}

	<input {...my_form_2.fields.baz.as('text')} />

	<p data-error>{error ? 'An error occurred' : 'No error'}</p>

	<button>submit</button>
</form>
